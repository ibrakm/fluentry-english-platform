// ─── Helpers ─────────────────────────────────────────────────────────────────

const SUPPORTED_LANGS: Language[] = ["en", "fr", "ar"];

function getLangFromPath(path: string): Language {
  const segment = path.split("/")[1] as Language;
  return SUPPORTED_LANGS.includes(segment) ? segment : "en";
}

function stripLangPrefix(path: string): string {
  const segment = path.split("/")[1];
  if (SUPPORTED_LANGS.includes(segment as Language)) {
    return path.replace(`/${segment}`, "") || "/";
  }
  return path;
}

